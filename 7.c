// Program showcasing use of increment and decrement unary operators
#include <stdio.h>
int main()
{
    int a=4; //variable declarotion
    printf("\n%d",a++); // post increment
    printf("\n%d",a);
    printf("\n%d",--a);// pre decrement
    printf("\n%d",a);
    printf("\n%d",++a);// pre increment
    printf("\n%d",a);
    printf("\n%d",a--);//post decrement
    printf("\n%d",a);
}
